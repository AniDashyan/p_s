#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	if (!src && !dest)
		return (NULL);
	while (i < n)
	{
		*(unsigned char *)dest = ((unsigned char *)src)[i];
		dest++;
		i++;
	}
	return (dest - n);
}
